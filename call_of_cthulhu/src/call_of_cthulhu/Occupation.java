package call_of_cthulhu;

public class Occupation {
	private int osp;
	private int crMin;
	private int crMax;
	private String name;
	
	public Occupation(int osp, int crMin, int crMax, String name) {
		this.osp = osp;
		this.crMin = crMin;
		this.crMax = crMax;
		this.name = name;
	}
}
